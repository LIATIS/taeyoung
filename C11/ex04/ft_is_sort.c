int	ft_is_sort(char *tab, int length, int (*f)(int, int))
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (i < length - 1)
	{
		if (j == 0)
			j = f(tab[i], tab[i + 1]);
		else if (j != f(tab[i], tab[i + 1]))
			return (0);
		i++;
	}
	return (1);
}
